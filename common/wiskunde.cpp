Matrix::Matrix(float N[][4])
{
	for ( int i = 0; i < 4; i++ )
	{
		for ( int j = 0; j < 4; j++ )
		{
			this->M[i][j] = N[i][j];
		}
	}
}

////////////////////////////////////////////////////////////////////////////////////
// Purpose : Fill the member matrix with the Identity Matrix
// Input   : -
// Output  : -
////////////////////////////////////////////////////////////////////////////////////
void Matrix::FillIndentity()
{
	for ( int i = 0; i < 4; i++ ) {
		for ( int j = 0; j < 4; j++ ) {
			if ( i == j )
				M[i][j] = 1;
			else
				M[i][j] = 0;
		}
	}
}

